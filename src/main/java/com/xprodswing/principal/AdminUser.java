    private void tableUpdate(){
    
        int cpt;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/xproddb", "root", ""); 
            pst = con.prepareStatement("SELECT * FROM user"); 
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rsmd = rs.getMetaData();
            cpt = rsmd.getColumnCount();
            DefaultTableModel dtm =(DefaultTableModel)jTableAdminUser.getModel();
            dtm.setRowCount(0);
            
            while(rs.next()){
            Vector vect = new Vector();
            //USERNAME,PASSWORD,AUTHORIZATION
            
            for(int i=1; i<= cpt; i++ ){
            vect.add(rs.getString("IDUSER"));
            vect.add(rs.getString("USERNAME"));
            vect.add(rs.getString("PASSWORD"));
            vect.add(rs.getString("AUTHORIZATION"));
            
            
                    }
            dtm.addRow(vect); 
            }
             
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(XprodDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(XprodDesktopAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
