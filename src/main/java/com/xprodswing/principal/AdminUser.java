    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        //Je récupère les infos des champs de texte 
        
        String usernameReg = txtUsernameReg.getText();
        String passwordReg = txtPasswordReg.getText();
        String authorizationReg = txtAuthorizationReg.getText();
        
        
            
                 
        try {   
            Class.forName("com.mysql.cj.jdbc.Driver");  //Enregistrer le driver JDBC 
            con = DriverManager.getConnection("jdbc:mysql://localhost/xproddb", "root", ""); 
            
            pst=con.prepareStatement("INSERT INTO user (USERNAME,PASSWORD,AUTHORIZATION) "
                    + "VALUES (?, ?, ?)"); 
            pst.setString(1,usernameReg);
            pst.setString(2,passwordReg);
            pst.setString(3,authorizationReg);
           
            
            pst.executeUpdate(); 
            
            JOptionPane.showMessageDialog(this, "Données enregistrées"); 
            
            //Je vide le contenu des champs remplis
            txtUsernameReg.setText("");
            txtPasswordReg.setText("");
            txtAuthorizationReg.setText("");
            
            
        } catch (SQLException ex) {
            Logger.getLogger(XprodDesktopClient.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(XprodDesktopClient.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_btnRegisterActionPerformed

