    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
       if(txtUsernameReg.getText().equals("") || txtPasswordReg.getText().equals("")){
       JOptionPane.showMessageDialog(null, "Entrez le nom d'utilisateur et le mot de passe", "Message", JOptionPane.INFORMATION_MESSAGE);
       }else{
           //listLogin = udao.rechercheLogin(txtUsername.getText(), DataEncryption.getSHA1(txtPassword.getText()));
           listLogin = udao.rechercheLogin(txtUsernameReg.getText(), txtPasswordReg.getText()); //Sans crypto
           if(listLogin.size()>=1){
               if(listLogin.get(0).getAuthorization().equalsIgnoreCase("administrateur")){
                   //Page des administrateurs
                   XprodDesktopAdmin desktopAdmin = new XprodDesktopAdmin();
                   desktopAdmin.setVisible(true);
                   desktopAdmin.afficherUser(txtUsernameReg.getText());
                   dispose();
               }else{
                   XprodDesktopClient desktopClient = new XprodDesktopClient();
                   desktopClient.setVisible(true);
                   desktopClient.afficherUser(txtUsernameReg.getText());
                   dispose();
               }
           }
       }
    }//GEN-LAST:event_btnLoginActionPerformed

